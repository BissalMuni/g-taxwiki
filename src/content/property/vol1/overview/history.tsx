/** 연도별 개정 항목: 문자열 또는 본문 + 하위목록 */
type Item = string | { text: string; sub: string[] };
type YearEntry = { year: string; items: Item[] };

function YearList({ entries }: { entries: YearEntry[] }) {
  return (
    <div className="space-y-3">
      {entries.map((e) => (
        <div key={e.year} style={{ display: "flex", gap: "12px" }}>
          <div style={{ flex: "0 0 64px", fontWeight: "bold", color: "#1890ff" }}>{e.year}</div>
          <ul style={{ margin: 0, paddingLeft: "1.1em", lineHeight: 1.7, flex: 1 }}>
            {e.items.map((it, i) =>
              typeof it === "string" ? (
                <li key={i}>{it}</li>
              ) : (
                <li key={i}>
                  {it.text}
                  <ul style={{ margin: "2px 0", paddingLeft: "1.1em" }}>
                    {it.sub.map((s, j) => (
                      <li key={j} style={{ color: "#595959" }}>{s}</li>
                    ))}
                  </ul>
                </li>
              ),
            )}
          </ul>
        </div>
      ))}
    </div>
  );
}

